package flightsSearch.model;

public class RouteFare {

	private Integer outboundFare;
	private Integer inboundFare;

	public Integer getOutboundFare() {
		return outboundFare;
	}

	public void setOutboundFare(Integer outboundFare) {
		this.outboundFare = outboundFare;
	}

	public Integer getInboundFare() {
		return inboundFare;
	}

	public void setInboundFare(Integer inboundFare) {
		this.inboundFare = inboundFare;
	}

}
